#!/bin/bash

echo "Поиск пустых каталогов в текущей директории и поддиректориях..."
echo "=============================================================="

# Используем find для поиска пустых каталогов
empty_dirs=$(find . -type d -empty)

if [ -z "$empty_dirs" ]; then
    echo "Пустых каталогов не найдено."
else
    echo "Найдены пустые каталоги:"
    echo "$empty_dirs"
    
    # Подсчет количества пустых каталогов
    count=$(echo "$empty_dirs" | wc -l)
    echo "=============================================================="
    echo "Всего пустых каталогов: $count"
fi
