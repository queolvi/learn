#!/bin/bash

# Переходим в директорию, где нужно выполнить операцию
cd ~/prog/learn/learn_cpp/15_algos

# Проходим по всем подкаталогам
for dir in */; do
    # Проверяем, что это действительно директория
    if [ -d "$dir" ]; then
